<?php

class Estabelecimentos_Walmart {
    public function Dados_Estabelecimento_Walmart($id) {
        include_once '../config/database_mysql.php';
        $pdo = Database::connect();
        $pdo->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
        $sql = "select id, cod_estabelecimento, desc_estabelecimento from wal_estabelecimento_2016 where cod_estabelecimento = ?";
        $q = $pdo->prepare($sql);
        $q->execute(array($id));
        $data = $q->fetch(PDO::FETCH_ASSOC);
        Database::disconnect();
        return $data;
    }
}
